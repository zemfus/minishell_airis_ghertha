#include "libft.h"

int	ft_is_allnum(char **str)
{
	int	i;

	if (!str)
		return (-1);
	i = 0;
	while (str[i] != NULL)
	{
		if ((ft_strnum(str[i])) == -1)
			return (-1);
		i++;
	}
	return (1);
}
