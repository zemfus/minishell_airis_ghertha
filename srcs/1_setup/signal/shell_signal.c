#include "../../../includes/minishell.h"

extern t_conf	g_sh;

void	sigint_handler(int signum)
{
	if (signum == SIGINT)
	{
		g_sh.exit_status = 1;
		printf("\n");
		rl_on_new_line();
		rl_replace_line("", 0);
		rl_redisplay();
	}
}

void	set_signal(void)
{
	signal(SIGINT, sigint_handler);
	signal(SIGQUIT, SIG_IGN);
}
